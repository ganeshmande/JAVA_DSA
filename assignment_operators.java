import java.util.*;
public class assignment_operators
{
    public static void main(String[] args)
    {
        int a=10;
        a+= 20;    //a=a+20
        System.out.println(a);

        int b=11;
        b-=10;    //b=b-10
        System.out.println(b);

        int c=50;
        c*=10;    //c=c*10
        System.out.println(c);

        int d=100;
        d/=10;    //d=d/10
        System.out.println(d);        
    }
}